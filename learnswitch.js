const color = 'red'
switch(color){
    case 'red':
        console.log('Color is green')
        break;
    case 'blue':
        console.log('Color is blue')
        break;
    case 'green':
        console.log('color is green')
        break;
    default:
        console.log('color is not red, blue or green')

}

